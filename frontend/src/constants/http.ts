/**
 * @file Семантические константы HTTP-статусов, используемых в обработке
 * доменных ошибок.
 */

/**
 * 401 Unauthorized — сервер сообщает, что запрос не аутентифицирован
 * (отсутствует или просрочена session-кука). На этот код подписана глобальная
 * auth-шина: при его получении пользователь сбрасывается, а роутер уходит
 * на страницу логина.
 */
export const HTTP_STATUS_UNAUTHORIZED = 401

/**
 * 409 Conflict — сервер сообщает, что запрос конфликтует с текущим состоянием
 * ресурса (например, попытка стартовать вторую сессию или отправить сообщение
 * в момент стрима ответа модели).
 */
export const HTTP_STATUS_CONFLICT = 409
